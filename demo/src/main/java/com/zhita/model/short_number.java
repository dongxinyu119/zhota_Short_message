package com.zhita.model;

public class short_number {
	
	private Integer short_number_id;//电话表
	
	private String short_send_type;//发送状态
	
	private Integer label_id;//标签ID
	
	private String short_numbers;//手机号
	
	private Integer short_message_id;//短信ID

	public Integer getShort_number_id() {
		return short_number_id;
	}

	public void setShort_number_id(Integer short_number_id) {
		this.short_number_id = short_number_id;
	}

	public String getShort_send_type() {
		return short_send_type;
	}

	public void setShort_send_type(String short_send_type) {
		this.short_send_type = short_send_type;
	}

	public Integer getLabel_id() {
		return label_id;
	}

	public void setLabel_id(Integer label_id) {
		this.label_id = label_id;
	}

	public String getShort_numbers() {
		return short_numbers;
	}

	public void setShort_numbers(String short_numbers) {
		this.short_numbers = short_numbers;
	}

	public Integer getShort_message_id() {
		return short_message_id;
	}

	public void setShort_message_id(Integer short_message_id) {
		this.short_message_id = short_message_id;
	}
	
	
	

}
